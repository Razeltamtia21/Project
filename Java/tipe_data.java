        short s = 10;
        System.out.println("Nilai short: " + s);
        System.out.println("Nilai max short: " + Short.MAX_VALUE);
        System.out.println("Nilai min short: " + Short.MIN_VALUE);
        System.out.println("Nilai short: " + Short.BYTES + " bytes");
        System.out.println("Nilai short: " + Short.SIZE + " bits");

        //long
        System.out.println("\n    Long");
        long l = 10;
        System.out.println("Nilai long: " + l);
        System.out.println("Nilai max long: " + Long.MAX_VALUE);
        System.out.println("Nilai min long: " + Long.MIN_VALUE);
        System.out.println("Nilai long: " + Long.BYTES + " bytes");
        System.out.println("Nilai long: " + Long.SIZE + " bits");

        //double
